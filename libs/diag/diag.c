void bytesswap(void *const _buf, const uint16_t _size)
{
	uint8_t tmp = 0;
	for (uint16_t n = 0; n < _size / 2; n++)
	{
		tmp = ((uint8_t *)_buf)[n];
		((uint8_t *)_buf)[n] = ((uint8_t *)_buf)[_size - 1 - n];
		((uint8_t *)_buf)[_size - 1 - n] = tmp;
	}
}

